import { axiosInstance } from "./request";

const input = document.getElementById("value");
const fileInput = document.getElementById("value1");
const displayImg = document.getElementById("displayimg");
const form = document.querySelector(".formms");

fileInput.addEventListener("change", function (e) {
  e.preventDefault();
  const file = this.files[0];
  if (file) {
    const reader = new FileReader();
    reader.onload = function (e) {
      displayImg.src = e.target.result;
      displayImg.style.display = "block";
    };
    reader.readAsDataURL(file);
  }
});

form.addEventListener("submit", function (e) {
  e.preventDefault();

  const formData = new FormData();
  formData.append("file", fileInput.files[0]);

  for (const pair of formData.entries()) {
    console.log(`${pair[0]}, ${pair[1].name}`);
  }

  const config = {
    headers: {
      "Content-Type": "multipart/form-data",
    },
  };

  axiosInstance
    .post("/image", formData, config)
    .then((response) => {
      const imageId = response.data.image_id;
      console.log("Rasm ID: ", imageId);

      localStorage.setItem("imageId", imageId.toString());
      location.href = "./logo.html";
    })
    .catch((error) => {
      console.log(error);
    });
});





